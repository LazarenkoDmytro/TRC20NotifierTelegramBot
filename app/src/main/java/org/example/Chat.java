package org.example;

/**
 * Represents a chat session with a unique identifier.
 * This simple class is used to parse a JSON query.
 */
public class Chat {
    // Unique identifier for the chat session
    private long id;

    /**
     * Retrieves the unique identifier for this chat session.
     *
     * @return The unique identifier (ID) of the chat session.
     */
    public long getId() {
        return id;
    }
}
